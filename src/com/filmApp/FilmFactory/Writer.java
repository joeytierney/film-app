package com.filmApp.FilmFactory;

public class Writer implements Crew {
	
	private String name;
	
	public Writer(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
