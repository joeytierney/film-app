package com.filmApp.FilmFactory;

public class Director implements Crew{
	
	private String name;
	
	public Director(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
